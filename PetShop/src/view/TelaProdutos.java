    public boolean tudoOK() {
        if (!jtxtCodigo.getText().isEmpty()
                && !jtxtDescricao.getText().isEmpty()
                && !jtxPreco.getText().isEmpty()
                && !jtxtValidade.getText().equals("  /  /    ")
                && !jtxtMarca.getText().isEmpty()
                && !jtxtModelo.getText().isEmpty()) {
            if(dataOK() && precoOK() && codValido){
                return true;
            }
        }
        return false;
    }

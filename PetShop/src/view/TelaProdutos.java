    private void jtxtCodigoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jtxtCodigoFocusLost
        if (criando) {
            if(geItens.codIsValido(jtxtCodigo.getText())){
                codValido = true;
            }else{
                codValido = false;
            }
            if(tudoOK()){
                jbEditar.setEnabled(true);
            }else if(!geItens.codIsValido(jtxtCodigo.getText())){
                JOptionPane.showMessageDialog(null, "Código já cadastrado");
                jtxtCodigo.setText(" ");
                jtxtCodigo.requestFocus();
            }
            else{
                jbEditar.setEnabled(false);
            }
        }
    }//GEN-LAST:event_jtxtCodigoFocusLost


    private void jbEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbEditarActionPerformed
        if (criando) {
            
                DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
                Produto p = new Produto();
                p.setCodProduto(jtxtCodigo.getText());
                p.setDescricao(jtxtDescricao.getText());
                p.setId(WIDTH);
                p.setMarca(jtxtMarca.getText());
                p.setModelo(jtxtModelo.getText());
                p.setPreco(Float.parseFloat(jtxPreco.getText()));
                p.setValidade(LocalDate.parse(jtxtValidade.getText(), formatter));
                geItens.cadastrarProduto(p);
                JOptionPane.showMessageDialog(this, "Produto Cadastrado");
                DefaultTableModel modelo = (DefaultTableModel) tabelaProdutos.getModel();
                modelo.addRow(new Object[]{
                    (geItens.proximoCodigo() - 1),
                    jtxtCodigo.getText(),
                    jtxtDescricao.getText(),
                    Float.parseFloat(jtxPreco.getText()),
                    jtxtValidade.getText(),
                    jtxtMarca.getText(),
                    jtxtModelo.getText()

                });
                limpaCampos();
                configurarCampos(false);
                configurarTabela(true);
                proximoCod();
                jbEditar.setEnabled(false);
                jbEditar.setText("Editar");
                jbEditar.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagens/DocumentEdit_40924.png")));
                jbExcluir.setText("Exluir");
                jbExcluir.setEnabled(false);
                criando = false;  
        } else if (editando){
            int x = tabelaProdutos.getSelectedRow();
            if(!geItens.codIsValido(jtxtCodigo.getText()) && !jtxtCodigo.getText().equals(tabelaProdutos.getValueAt(x, 1))){
                JOptionPane.showMessageDialog(null, "Código já cadastrado");
            }else if(!dataOK()){
                JOptionPane.showMessageDialog(null, "Formato de data Inválida");
            }else if(!precoOK()){
                JOptionPane.showMessageDialog(null, "Formato de Preço inválido");
            }
            else{
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        Produto p = new Produto();
        p.setDescricao(jtxtDescricao.getText());
        p.setMarca(jtxtMarca.getText());
        p.setModelo(jtxtModelo.getText());
        p.setPreco(Float.parseFloat(jtxPreco.getText()));
        p.setValidade(LocalDate.parse(jtxtValidade.getText(), formatter));
        p.setCodProduto(jtxtCodigo.getText());
        p.setId(Integer.parseInt(jtxtId.getText()));
        geItens.alterarProdutos(p);
        tabelaProdutos.setValueAt(jtxtDescricao.getText(), x, 2);
        tabelaProdutos.setValueAt(Float.parseFloat(jtxPreco.getText()), x, 3);
        tabelaProdutos.setValueAt(jtxtValidade.getText(), x, 4);
        tabelaProdutos.setValueAt(jtxtMarca.getText(), x, 5);
        tabelaProdutos.setValueAt(jtxtModelo.getText(), x, 6);
        JOptionPane.showMessageDialog(null, "Alterado com sucesso");
        limpaCampos();
            proximoCod();
            configurarCampos(false);
            configurarTabela(true);
            jbExcluir.setText("Excluir");
            jbExcluir.setForeground(Color.red);
            jbExcluir.setEnabled(false);
            jbExcluir.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagens/delete_remove_page_document_16678.png")));
            jbEditar.setText("Editar");
            jbEditar.setEnabled(false);
            criando = false;
            editando = false;
            jbLimpar.setEnabled(true);
            }
        }else{
            configurarCampos(true);
            configurarTabela(false);
            jbExcluir.setText("Cancelar");
             jbExcluir.setForeground(Color.red);
                 jbExcluir.setEnabled(true);
                jbEditar.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagens/save_3621.png")));
            jbEditar.setText("Salvar");
            jbEditar.setEnabled(true);
            editando = true;
            criando = false;
            jbLimpar.setEnabled(false);
        }

    }//GEN-LAST:event_jbEditarActionPerformed

